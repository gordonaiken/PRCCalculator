"""
Basic Calculator for two numbers which can perform the following operations:
addition, subtraction, multiplication and division
"""

def addition(num1, num2):

    return num1 + num2

def subtract(num1, num2):

    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

